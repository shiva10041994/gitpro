package com.oldProject.insert;

public class StringConcatation {
	
	public static void main(String[] args) {
		
		int x = 48;
		int y = 94;
		
		String s = "shiva";
		String f = "hello";
		
		System.out.println(x+y);
		System.out.println(s+f);
		System.out.println(s+f+x+y);
		System.out.println(x+y+s+f);
	}

}
